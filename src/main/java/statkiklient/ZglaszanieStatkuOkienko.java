    private byte[] wczytajZdjecie(String pathToPictureTextField) throws IOException {
        Path sciezka = Paths.get(pathToPictureTextField);
        byte[] img = Files.readAllBytes(sciezka);
        return img;
    }
    
    private void registerButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_registerButtonMouseClicked
        try {    
            int MMSI = Integer.parseInt(MMSITextField.getText());
            String name = nameTextField.getText();
            String flag = flagTextField.getText();
            String vType = vesselTypeTextField.getText();
            int grossTonnage = Integer.parseInt(grossTonnageTextField.getText());
            byte[] img = wczytajZdjecie(pathToPictureTextField.getText());
            StatkiUsluga_Service usluga = new StatkiUsluga_Service();
            usluga.getStatkiUslugaPort().dodajStatek(MMSI, name, flag, vType, img, grossTonnage);
            MMSITextField.setText("");
            nameTextField.setText("");
            flagTextField.setText("");
            vesselTypeTextField.setText("");
            grossTonnageTextField.setText("");
            pathToPictureTextField.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Wprowad≈∫ poprawne dane.");
        }
    }//GEN-LAST:event_registerButtonMouseClicked

    private void showPictureButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_showPictureButtonMouseClicked
        try {
            String poleAdresu = pathToPictureTextField.getText();
            byte[] obrazek = wczytajZdjecie(poleAdresu);
            ZdjecieStatkuOkienko zdjecie = new ZdjecieStatkuOkienko(obrazek);
            zdjecie.setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Nie ma takiego pliku.");
        }        
    }//GEN-LAST:event_showPictureButtonMouseClicked

