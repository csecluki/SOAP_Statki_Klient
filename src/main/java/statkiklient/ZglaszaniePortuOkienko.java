    private void registerButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_registerButtonMouseClicked
        try {
            String portName = portNameTextField.getText();
            String country = countryTextField.getText();
            float latitude = Float.parseFloat(latitudeTextField.getText());
            float longitude = Float.parseFloat(longitudeTextField.getText());
            StatkiUsluga_Service usluga = new StatkiUsluga_Service();
            usluga.getStatkiUslugaPort().dodajPort(portName, country, latitude, longitude);
            portNameTextField.setText("");
            countryTextField.setText("");
            latitudeTextField.setText("");
            longitudeTextField.setText("");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Wprowad≈∫ poporawne dane.");
        }
    }//GEN-LAST:event_registerButtonMouseClicked

